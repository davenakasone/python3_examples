class Charging:   
    
    #--------------------------------------------------------------------------------------------------------------------
    def __init__(self, amps = 200, volts = 1_000, watts = 2_000, chemical = 'lithium'):
        self.amps = amps
        self.volts = volts
        self.watts = watts
        self.chemical = chemical
        self.charge_time = 2

    #--------------------------------------------------------------------------------------------------------------------
    def cycles(self):
        if self.chemical == 'nicad':
            return 400
        elif self.chemical == 'lithium':
            return 900
        else:
            return "unknown"
    
    #--------------------------------------------------------------------------------------------------------------------
    def print_battery_specs(self):
        print(f"amps: {self.amps}, volts: {self.volts}, watts: {self.watts}, chemical: {self.chemical}")
    
    #--------------------------------------------------------------------------------------------------------------------
    def closest(self):               # notice that you still need the 'self' so it can point to the class
        return "San Fransisco, CA"
    
    #--------------------------------------------------------------------------------------------------------------------


