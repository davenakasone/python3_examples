class Car:

    #--------------------------------------------------------------------------------------------------------------------
    def __init__(self, make, model, year):
        self.make = make
        self.model = model
        self.year = year
        self.odometer = 0
        self.gas_tank = 20

    #--------------------------------------------------------------------------------------------------------------------
    def get_description(self):
        description = f"{self.year} {self.make} {self.model}"
        return description
    
    #--------------------------------------------------------------------------------------------------------------------
    def print_odometer(self):
        print("odometer: ",self.odometer)
    
    #--------------------------------------------------------------------------------------------------------------------
    def update_odometer(self, current_miles):
        if current_miles >= self.odometer:
            self.odometer = current_miles
        else:
            print("someone is trying to cheat odometer")

    #--------------------------------------------------------------------------------------------------------------------
    def increment_odometer(self, miles):
        if miles > 0:
            self.odometer += miles
        else:
            print("can't add negative values") 
    
    #--------------------------------------------------------------------------------------------------------------------
    def gas_tank_specs(self):
        print("this vehicle has a",self.gas_tank,"gallon tank")
    
    #--------------------------------------------------------------------------------------------------------------------
    def print_mpg(self):
        print("this car can go 12 miles per gallon")
        
